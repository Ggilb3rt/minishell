#include "minishell.h"

int	ft_isnbr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_isdigit(str[i]) || str[0] == '-')
			i++;
		else
			return (0);
	}
	return (1);
}
